    /**
     * Checks all the user input before trying to create a student: whether the
     * textFields are empty , and also for age and skillRating whether an
     * integer was input. Appropriate messages will be displayed
     * @return Returns true if all the input is correct and the student can be
     * created, returns false if some input is not correct
     */
    public boolean checkInput(){
        boolean input = false;
        if (forenameTxtField.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Please enter the first name of the Student");
        }
        else if(surnameTxtField.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Please enter the last name of the Student");
        }
        else {
            input = true;
        }
        return input;
    }

    /**
     * When a student is successfully created this method will clear all the 
     * input fields so it is easier to fill the information of a new student
     */
