    private void selectGroupBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_selectGroupBtnActionPerformed
        if (groupsList.getSelectedIndex() == -1) {
            JOptionPane.showMessageDialog(null, "Please select a group");
        } else {
            showMembers(groupsList.getSelectedIndex());
        }
    }//GEN-LAST:event_selectGroupBtnActionPerformed

    private void removeMemberBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeMemberBtnActionPerformed
        if (groupsList.getSelectedIndex() == -1) {
            JOptionPane.showMessageDialog(null, "Please select a group");
        }
        else if(teamMembersList.getSelectedIndex() == -1){
            JOptionPane.showMessageDialog(null, "Please select a student to remove");
        }
        else{
            MainMenu.groupGenerator.removeStudentFromGroup(groupsList.getSelectedIndex(), teamMembersList.getSelectedIndex());
            JOptionPane.showMessageDialog(null, "Student successfully removed from the group");
            refreshLists();
        }
    }//GEN-LAST:event_removeMemberBtnActionPerformed

    private void allocateStudentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_allocateStudentActionPerformed
        if (groupsList.getSelectedIndex() == -1) {
            JOptionPane.showMessageDialog(null, "Please select a group");
        }
        else if(studentsWithNoGroupList.getSelectedIndex() == -1){
            JOptionPane.showMessageDialog(null, "Please select a student");
        }
        else{
            MainMenu.groupGenerator.addStudentToGroup(groupsList.getSelectedIndex(),studentsWithNoGroupList.getSelectedIndex());
            JOptionPane.showMessageDialog(null, "Student successfully added");
            refreshLists();
        }
    }//GEN-LAST:event_allocateStudentActionPerformed


