    /**
     * Shows the members of a specific group in a list
     * @param index defines which group to show the members of by telling 
     * the index number of the group in the groups ArrayList
     */
    public void showMembers(int index) {
        teamMembersList.setModel(new javax.swing.AbstractListModel<String>() {
            String[] strings = MainMenu.groupGenerator.getGroup(index).getStudentsInfo();
            public int getSize() {
                return strings.length;
            }
            public String getElementAt(int i) {
                return strings[i];
            }
        });
    }

