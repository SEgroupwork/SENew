    private void createStudentsBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createStudentsBtnActionPerformed
        if (checkInput()) {
            String firstname = forenameTxtField.getText();
            String lastname = surnameTxtField.getText();
            int age = 0;
            try {
                age = Integer.parseInt(ageTxtField.getText());
                if (requirementsBtn.isSelected()) {
                    String skill = "Requirements Gathering and Design";
                    int skillRating = 0;
                    try {
                        skillRating = Integer.parseInt(reqTxtField.getText());
                        Student newStudent = new Student(firstname, lastname, age, skill, skillRating);
                        MainMenu.groupGenerator.students.addStudent(newStudent, 1);
                        JOptionPane.showMessageDialog(null, "Student was successfully added");
                        clearInput();

                    } catch (Exception e) {
                        JOptionPane.showMessageDialog(null, "Sorry you did not input a correct skill rating, this must be a number between 1 and 10, please try again");
                        reqTxtField.setText("");
                    }

                } else if (implementationBtn.isSelected()) {
                    String skill = "Implementation and Testing";
                    int skillRating = 0;
                    try {
                        skillRating = Integer.parseInt(implementationTxtField.getText());
                        Student newStudent = new Student(firstname, lastname, age, skill, skillRating);
                        MainMenu.groupGenerator.students.addStudent(newStudent, 2);
                        JOptionPane.showMessageDialog(null, "Student was successfully added");
                        clearInput();
                    } catch (Exception e) {
                        JOptionPane.showMessageDialog(null, "Sorry you did not input a correct skill rating, this must be a number between 1 and 10, please try again");
                        implementationTxtField.setText("");
                    }

                } else if (documentationBtn.isSelected()) {
                    String skill = "Documentation and Maintenance";
                    int skillRating = 0;
                    try {
                        skillRating = Integer.parseInt(documentationTxtField.getText());
                        Student newStudent = new Student(firstname, lastname, age, skill, skillRating);
                        MainMenu.groupGenerator.students.addStudent(newStudent, 3);
                        JOptionPane.showMessageDialog(null, "Student was successfully added");
                        clearInput();
                    } catch (Exception e) {
                        JOptionPane.showMessageDialog(null, "Sorry you did not input a correct skill rating, this must be a number between 1 and 10, please try again");
                        documentationTxtField.setText("");
                    }

                } else {
                    JOptionPane.showMessageDialog(null, "Please Select a skill");
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Sorry you did not input a correct age, please try again");
                ageTxtField.setText("");
            }

        }

    }//GEN-LAST:event_createStudentsBtnActionPerformed

